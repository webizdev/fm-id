let currentLang = localStorage.getItem('fm_lang') || 'id';

function applyTranslations() {
    // 1. Static UI elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[currentLang][key];
            } else {
                // Use innerHTML for keys that might have entities like &copy;
                if (key.includes('footer_copyright') || key.includes('section_')) {
                    el.innerHTML = translations[currentLang][key];
                } else {
                    el.innerText = translations[currentLang][key];
                }
            }
        }
    });

    // 2. Handle RTL for Arabic
    if (currentLang === 'ar') {
        document.body.dir = "rtl";
        document.body.classList.add('rtl');
    } else {
        document.body.dir = "ltr";
        document.body.classList.remove('rtl');
    }

    // 3. Dispatch event for dynamic content (Grids)
    window.dispatchEvent(new CustomEvent('langChanged', { detail: { lang: currentLang } }));
}

function switchLanguage(lang) {
    if (translations[lang]) {
        currentLang = lang;
        localStorage.setItem('fm_lang', lang);
        applyTranslations();
    }
}

// Initial detection if no preference
// We prioritize 'id' (Indonesian) for this specific business unless the user explicitly chose otherwise.
if (!localStorage.getItem('fm_lang')) {
    // We default to ID. We skip auto-detection for now to ensure local users aren't served English unexpectedly.
    currentLang = 'id';
    localStorage.setItem('fm_lang', currentLang);
}

document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();

    // Add event listeners to any language selectors if they exist
    const langSelect = document.getElementById('lang-switch');
    if (langSelect) {
        langSelect.value = currentLang;
        langSelect.addEventListener('change', (e) => switchLanguage(e.target.value));
    }
});
